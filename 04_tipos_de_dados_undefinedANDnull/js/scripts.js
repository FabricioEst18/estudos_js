// null: representa a ausência intencional de um valor. É um valor atribuído a uma variável para indicar que ela não possui um valor válido ou que o valor é desconhecido.
var nome = null;

console.log(nome); // null

nome = "João";

console.log(nome); // João

// undefined: representa a ausência de um valor ou a falta de definição de uma variável. É o valor padrão atribuído a uma variável que foi declarada, mas não inicializada.
var idade;

console.log(idade); // undefined

idade = 30;

console.log(idade); // 30
